package com.cic.incidencias.datosAjax;

public class Genericos {
    private String fechIni;
    private String fechFin;
    private String horIni;
    private String horFin;
    private String obs;
    
    public String getFechIni() {
        return fechIni;
    }
    public void setFechIni(String fechIni) {
        this.fechIni = fechIni;
    }
    public String getFechFin() {
        return fechFin;
    }
    public void setFechFin(String fechFin) {
        this.fechFin = fechFin;
    }
    public String getHorIni() {
        return horIni;
    }
    public void setHorIni(String horIni) {
        this.horIni = horIni;
    }
    public String getHorFin() {
        return horFin;
    }
    public void setHorFin(String horFin) {
        this.horFin = horFin;
    }
    public String getObs() {
        return obs;
    }
    public void setObs(String obs) {
        this.obs = obs;
    }

    
}
